function updateLanguageButton(){

    const button =
    document.querySelector("#languageBtn");

    if(!button) return;

    button.innerHTML = `
        <span class="lang-flag">
            ${LANG[currentLanguage].meta.flag}
        </span>

        <span class="lang-name">
            ${LANG[currentLanguage].meta.native}
        </span>

        <i class="fa-solid fa-chevron-down"></i>
    `;

}

/*=========================================
UPDATE LANGUAGE MENU
=========================================*/

function updateLanguageMenu(){

    document
    .querySelectorAll("[data-language]")
    .forEach(item=>{

        const code =
        item.dataset.language;

        item.classList.remove(
            "active-language"
        );

        if(code===currentLanguage){

            item.classList.add(
                "active-language"
            );

        }

    });

}

/*=========================================
CREATE LANGUAGE EVENTS
=========================================*/

function bindLanguageEvents(){

    document
    .querySelectorAll("[data-language]")
    .forEach(item=>{

        item.onclick=()=>{

            const lang =
            item.dataset.language;

            setLanguage(lang);

        };

    });

}

