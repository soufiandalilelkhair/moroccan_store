let currentLanguage =
localStorage.getItem("lang") || null;

/*=========================================
GET TRANSLATION
=========================================*/

function t(path){

const keys = path.split(".");

let value = LANG[currentLanguage];

for(const key of keys){

if(
value &&
value[key] !== undefined
){

value = value[key];

}else{

return path;

}

}

return value;

}

/*=========================================
SAVE LANGUAGE
=========================================*/

function saveLanguage(lang){

localStorage.setItem(
"lang",
lang
);

localStorage.setItem(
"direction",
LANG[lang].meta.dir
);

localStorage.setItem(
"font",
LANG[lang].meta.font
);

localStorage.setItem(
"flag",
LANG[lang].meta.flag
);

}

/*=========================================
LOAD LANGUAGE
=========================================*/

function loadSavedLanguage(){

const lang =
localStorage.getItem("lang");

if(lang && LANG[lang]){

currentLanguage = lang;

}else{

detectBrowserLanguage();

}

}

/*=========================================
DETECT BROWSER LANGUAGE
=========================================*/

function detectBrowserLanguage(){

const browser =
navigator.language
.toLowerCase();

if(browser.startsWith("fr")){

currentLanguage = "fr";

}

else if(browser.startsWith("es")){

currentLanguage = "es";

}

else if(browser.startsWith("de")){

currentLanguage = "de";

}

else if(browser.startsWith("en")){

currentLanguage = "en";

}

else{

currentLanguage = "ar";

}

saveLanguage(currentLanguage);

}

/*=========================================
SET LANGUAGE
=========================================*/

function setLanguage(lang){

if(!LANG[lang]) return;

currentLanguage = lang;

saveLanguage(lang);

applyLanguage();

}

